public class EvenOdd {
    public static void main(String[] args) {

        // Hardcoded number
        int number = 10;

        // Check whether the number is even or odd
        if (number % 2 == 0) {
            System.out.println(number + " is an Even number.");
        } else {
            System.out.println(number + " is an Odd number.");
        }
    }
}